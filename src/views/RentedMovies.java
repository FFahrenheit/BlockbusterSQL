    private void returnMovieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnMovieActionPerformed
        // TODO add your handling code here:  

        int input = JOptionPane.showConfirmDialog(null, "Desea devolver ya este titulo?");
        if(input == 0)
        {
            if(this.session.db().returnMovie(selected))
            {
                JOptionPane.showMessageDialog(this, "Ha regresado la pel√≠cula con exito","Renta lista",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "No se pudo regresar. Intente despues","Error",JOptionPane.ERROR_MESSAGE);
            }
            this.movieTable.setModel(this.session.getRentedTable());
        }
    }//GEN-LAST:event_returnMovieActionPerformed

