    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        if(selected != null)
        {
            int input = JOptionPane.showConfirmDialog(null, "Desea este registro?");
            if(input == 0)
            {
                if(session.db().deleteRent(selected))
                {
                    JOptionPane.showMessageDialog(this, "Se ha eliminado el registro","Eliminado",JOptionPane.INFORMATION_MESSAGE);

                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Verifique los campos","No se pudo eliminar",JOptionPane.ERROR_MESSAGE);
                }
                clean();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Seleccione un registro","No se puede eliminar",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteActionPerformed

