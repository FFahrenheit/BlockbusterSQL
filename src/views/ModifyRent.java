    private void rentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rentTableMouseClicked
        // TODO add your handling code here:
        int seleccion = rentTable.rowAtPoint(evt.getPoint());
        movieF.setText(rentTable.getValueAt(seleccion, 1).toString());
        userF.setText(rentTable.getValueAt(seleccion, 2).toString());
        startDate.setText(rentTable.getValueAt(seleccion, 3).toString());
        endDate.setText(rentTable.getValueAt(seleccion, 4).toString());

        selected = Integer.parseInt(rentTable.getValueAt(seleccion, 0).toString());
    }//GEN-LAST:event_rentTableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(selected != null)
        {
            String days = this.daysF.getText();
            if(days != null && !days.trim().isEmpty())
            {
                int input = JOptionPane.showConfirmDialog(null, "Desea atrasar la fecha de entrega " + days + " horas?");
                if(input == 0)
                {
                    if(session.db().updateRent(selected,days))
                    {
                        JOptionPane.showMessageDialog(this, "Se ha editador el registro","Editado",JOptionPane.INFORMATION_MESSAGE);

                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Verifique los campos","No se pudo editar",JOptionPane.ERROR_MESSAGE);
                    }
                    clean();
                }
            }

        }
        else
        {
            JOptionPane.showMessageDialog(this, "Seleccione un registro","No se puede editar",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

